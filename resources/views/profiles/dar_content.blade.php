    $(document).ready(() => {

    $('.milestone-submilestones').on( 'click', function (e) {
        e.preventDefault();

        if (typeof $(this).attr('data-modal_title') !== 'undefined'){
            $('#modal-milestone-submilestones').modal('show');

            var bg = $(this).find('.milestone-bg').css('background-image');

            $('#modal-milestone-submilestones').find('ul').addClass('d-none');
            $('#modal-milestone-submilestones').find('.ul-group-label').css('background-image', bg)
            $('#modal-milestone-submilestones').find('.ul-group-label').css('opacity', 0.7)

            $('#' + $(this).attr('data-modal_ul')).removeClass('d-none');

            $('#modal-milestone-submilestones').find('.modal-header').css('background-image', bg);
            $('#modal-milestone-submilestones').find('#header_total ').text($(this).find('.widget-numbers').text());
            $('#modal-milestone-submilestones').find('.modal-title').text($(this).attr('data-modal_title'));
        }

    });


    $('.circle-progress').each(function(index, element){
        var progress = $(element).find('.site_progress').text();

        // console.log(progress);

        $(element)
            .circleProgress({
            value: progress,
            size: 50,
            lineCap: "round",
            fill: { gradient: ["#ff1e41"] },
            })
            .on("circle-animation-progress", function (event, progress, stepValue) {
            $(this)
                .find("small")
                .html("<span>" + stepValue.toFixed(2).substr(2) + "%<span>");
            });

    });

    });


</script>
